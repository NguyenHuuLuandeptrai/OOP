
class KethuaVidu1 extends Vidu1 {
	private int c;
	public KethuaVidu1(int c){
		this.c = c;
	} 
	@Override
	public int TinhTong(){
		int tong=0;
	return tong = getA() + getB() + this.c;
		
	}
	
	@Override
	public int TinhTich(){
		int tich = 0;
	return	tich = getA() * getB()*this.c;
	}
}
